let wrapperTed = document.getElementById('wrapperTed');
let ted = document.getElementById('ted');
ted.addEventListener("mouseover", popUpTed);
ted.addEventListener("mouseout", popUpNoneTed);
function popUpTed() {
    wrapperTed.setAttribute("style", "display:block");
}
function popUpNoneTed() {
 wrapperTed.style.display = "none";  
}

